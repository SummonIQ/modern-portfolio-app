type SocialLink = {
  id: string;
  platform: string;
  url: string;
  icon?: string | null;
};

function BrandIcon({ name, size = 18 }: { name: string; size?: number }) {
  const path = BRAND_PATHS[name];
  if (!path) return null;
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="currentColor"
      aria-hidden="true"
    >
      <path d={path} />
    </svg>
  );
}

function pickIconRenderer(
  platform: string,
  icon?: string | null,
): { kind: "brand"; key: string } | { kind: "lucide"; Icon: IconComponent } {
  const key = (icon || platform).toLowerCase().trim();
  if (BRAND_PATHS[key]) return { kind: "brand", key };
  if (key === "email" || key === "mail") return { kind: "lucide", Icon: Mail };
  return { kind: "lucide", Icon: Globe };
}

export function SocialIcons({
  links,
  className,
  size = 18,
}: {
  links: SocialLink[];
  className?: string;
  size?: number;
}) {
  if (!links.length) return null;
  return (
    <ul className={cn("flex items-center gap-2", className)}>
      {links.map((link) => {
        const r = pickIconRenderer(link.platform, link.icon);
        return (
          <li key={link.id}>
            <a
              href={link.url}
              target="_blank"
              rel="noopener noreferrer"
              aria-label={link.platform}
              className="inline-flex size-9 items-center justify-center rounded-lg border border-border text-foreground/70 transition-all hover:-translate-y-0.5 hover:border-brand-400/40 hover:bg-brand-400/10 hover:text-foreground"
            >
              {r.kind === "brand" ? (
                <BrandIcon name={r.key} size={size} />
              ) : (
                <r.Icon size={size} />
              )}
            </a>
          </li>
        );
      })}
    </ul>
  );
}
